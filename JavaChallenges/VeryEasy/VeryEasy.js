let name1 = "John";
let name2 = "Sarah";

if (name1.length >
name2.length) {
    console.log(name1 + "is longer than" + name2 + "by" +
    (name1.length - name2.length)+ "characters.");
} else {
    console.log(name2 + "is longer than" + name1 + "by" + 
    (name2.length - name1.length) + "characters.");
}
